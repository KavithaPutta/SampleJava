package corejava;

public class MarksCalculation {
	static int sub1=85;
	static int sub2=90;
	static int sub3=80;
	public static void totalMarks() {
		System.out.println(sub1+sub2+sub3);
	}
	public static void averageMarks() {
		System.out.println(sub1+sub2+sub3/3);
	}
	

	public static void main(String[] args) {
		totalMarks();
		averageMarks();
			
		}
		

	}


